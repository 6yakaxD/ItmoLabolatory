package Collection;

public class Coordinates {
    private int x;
    private Float y; //Максимальное значение поля: 617, Поле не может быть null


    public int getX()
    {
        return x;
    }
    public Float getY()
    {
        return y;
    }

    public void setX(int x)
    {
        this.x = x;
    }
    public void setY(Float y)
    {
        this.y = y;
    }

}
